// ==================== CURRENT LANGUAGE ====================
let currentLanguage = localStorage.getItem('preferredLanguage') || 'en';

// ==================== TRANSLATION FUNCTION ====================
function translatePage() {
  const elements = document.querySelectorAll('[data-translate]');
  
  elements.forEach(element => {
    const key = element.getAttribute('data-translate');
    let translation = translations[currentLanguage]?.[key];
    
    if (translation !== undefined && translation !== null) {
      // Special handling for elements that might have HTML
      if (element.tagName === 'H1' && key === 'hero_title') {
        // Handle HTML in hero title
        element.innerHTML = translation;
      } else if (element.tagName === 'SPAN' && element.classList.contains('level-badge')) {
        // Handle badge text
        element.textContent = translation;
      } else if (element.tagName === 'BUTTON' && element.classList.contains('enroll-btn')) {
        // Handle button with HTML
        element.innerHTML = translation;
      } else if (element.tagName === 'A' && element.classList.contains('back-link')) {
        // Handle back link with HTML
        element.innerHTML = translation;
      } else {
        // Default text content
        element.textContent = translation;
      }
    }
  });
  
  // Update active language button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    const lang = btn.getAttribute('data-lang');
    if (lang === currentLanguage) {
      btn.classList.add('active');
    } else {
      btn.classList.remove('active');
    }
  });
  
  // Save to localStorage
  localStorage.setItem('preferredLanguage', currentLanguage);
  
  // Optional: console log for debugging
  console.log(`Language changed to: ${currentLanguage}`);
}

// ==================== CHANGE LANGUAGE FUNCTION ====================
function setLanguage(lang) {
  if (!translations[lang]) {
    console.warn(`Language ${lang} not found`);
    return;
  }
  currentLanguage = lang;
  translatePage();
}

// ==================== INITIALIZE ON PAGE LOAD ====================
document.addEventListener('DOMContentLoaded', () => {
  // Get saved language or default to 'en'
  const savedLang = localStorage.getItem('preferredLanguage');
  if (savedLang && translations[savedLang]) {
    currentLanguage = savedLang;
  }
  
  // Apply translations
  translatePage();
  
  // Add event listeners to language buttons
  const langBtns = document.querySelectorAll('.lang-btn');
  langBtns.forEach(btn => {
    // Remove existing listeners to avoid duplicates
    btn.removeEventListener('click', btn._listener);
    // Add new listener
    const listener = () => {
      const lang = btn.getAttribute('data-lang');
      setLanguage(lang);
    };
    btn.addEventListener('click', listener);
    btn._listener = listener;
  });
  
  console.log('Translation system initialized');
});

// Make functions globally available
window.setLanguage = setLanguage;
window.currentLanguage = () => currentLanguage;